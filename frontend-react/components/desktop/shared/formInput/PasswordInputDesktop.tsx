import React, { useState } from 'react'
import { EyeOffPasswordDesktop, EyeOnPasswordDesktop, PasswordGeneratorDesktop } from '@/components/assets/iconsDesktop'
import { validatePassword } from '@/components/desktop/commonDesktop/validate/validatePasswordDesktop'
import { generatePassword } from '@/components/desktop/commonDesktop/generatePassword'

interface IPasswordInput {
    value: string
    onChange: (value: string) => void
    onError: (value: string) => void
    label: string
    placeholder: string
    externalError?: string | null
    inputClassName?: string
    labelClassName?: string
    errorClassName?: string
    inputERRAddStyle?: string
    inputNOERRAddStyle?: string
    showGenerateButton?: boolean
    required?: boolean
    formError?: boolean // Добавляем пропс для ошибки формы
}

const PasswordInputDesktop: React.FC<IPasswordInput> = ({
    value,
    onChange,
    onError,
    label,
    placeholder,
    externalError,
    inputClassName = '',
    labelClassName = '',
    errorClassName = '',
    inputERRAddStyle = '',
    inputNOERRAddStyle = '',
    showGenerateButton = false,
    required = false,
    formError = false, // Ошибка формы
}) => {
    const [showPassword, setShowPassword] = useState(false)
    const [internalError, setInternalError] = useState<string | null>(null)
    const [touched, setTouched] = useState(false)

    const handleGeneratePassword = () => {
        const newPassword = generatePassword()
        onChange(newPassword)
        setInternalError(null)
    }

    const handleBlur = () => {
        setTouched(true)
        const error = validatePassword(value).textError
        setInternalError(error)
        onError(error || '')
    }

    const handleChange = (e: React.ChangeEvent<HTMLInputElement>) => {
        const newValue = e.target.value
        onChange(newValue)

        if (touched) {
            const { textError } = validatePassword(newValue)
            setInternalError(textError)
            onError(textError || '')
        }
    }

    // Определяем есть ли ошибка (включая ошибку формы)
    const hasError = Boolean(externalError || internalError || (formError && !value.trim()))

    return (
        <div className="flex w-full flex-col gap-1.5">
            <label htmlFor={label} className={labelClassName}>
                {label}
            </label>
            <div className="relative flex w-full">
                <input
                    id={label}
                    type={showPassword ? 'text' : 'password'}
                    placeholder={placeholder}
                    value={value}
                    onChange={handleChange}
                    onBlur={handleBlur}
                    className={`w-full ${inputClassName} ${hasError ? inputERRAddStyle : inputNOERRAddStyle}`}
                />
                <button
                    type="button"
                    onClick={() => setShowPassword((prev) => !prev)}
                    className="absolute right-4 top-1/2 flex -translate-y-1/2 items-center text-[#878797]"
                >
                    {showPassword ? <EyeOnPasswordDesktop /> : <EyeOffPasswordDesktop />}
                </button>
                {showGenerateButton && (
                    <button
                        type="button"
                        onClick={handleGeneratePassword}
                        className="absolute right-[-10%] top-1/2 flex -translate-y-1/2 items-center text-[#878797]"
                    >
                        <PasswordGeneratorDesktop />
                    </button>
                )}
            </div>
            {(internalError || externalError) && <p className={errorClassName}>{internalError || externalError}</p>}
        </div>
    )
}

export default PasswordInputDesktop
